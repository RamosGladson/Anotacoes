a = input('\033[4mDigite algo:\033[0m ')
print('É alfanumerico? \033[1;31m{}\033[m'.format(a.isalnum()))
print('É somente numeros? \033[1;32m{}\033[m'.format(a.isnumeric()))
print('É somente letras? \033[1;33m{}\033[m'.format(a.isalpha()))
print('É inteiro? \033[1;34m{}\033[m'.format(a.isdecimal()))
print('Tem somente letras minúsculas? \033[1;35m{}\033[m'.format(a.islower()))
print('Tem somente letras maiúsculas? \033[1;36m{}\033[m'.format(a.isupper()))